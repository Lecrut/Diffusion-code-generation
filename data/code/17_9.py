def is_even(n):
    return n % 2 == 0
if __name__ == '__main__':
    number1 = 4
    number2 = 7
    number3 = 0
    number4 = -2
    print(f"Is {number1} even? {is_even(number1)}")
    print(f"Is {number2} even? {is_even(number2)}")
    print(f"Is {number3} even? {is_even(number3)}")
    print(f"Is {number4} even? {is_even(number4)}")