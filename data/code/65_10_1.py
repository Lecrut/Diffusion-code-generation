def access_list_by_index():
    my_list = [10, 20, 30, 40, 50]
    first_element = my_list[0]
    middle_element = my_list[2]
    last_element = my_list[4]
    print(f"The list is: {my_list}")
    print(f"The first element (index 0) is: {first_element}")
    print(f"The middle element (index 2) is: {middle_element}")
    print(f"The last element (index 4) is: {last_element}")
if __name__ == '__main__':
    access_list_by_index()