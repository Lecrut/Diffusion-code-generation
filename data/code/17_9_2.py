def is_even(n):
    return n % 2 == 0
if __name__ == '__main__':
    num1 = 4
    num2 = 7
    num3 = 0
    num4 = -2
    print(f"Is {num1} even? {is_even(num1)}")
    print(f"Is {num2} even? {is_even(num2)}")
    print(f"Is {num3} even? {is_even(num3)}")
    print(f"Is {num4} even? {is_even(num4)}")